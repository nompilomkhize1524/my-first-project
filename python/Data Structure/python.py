#Loops  Control statements

fruits = ["apple", "banana", "cherry", "date"]


for fruit in fruits:
    if fruit == "cherry":
        break #Exit loop if cherry is found
    print (fruit)
    
    
    for fruit in fruits:
        if fruit == "cherry":
            continue #Skip cherry and moves to the iteration
        print(fruit)
        
        
        
        
    
    
     
